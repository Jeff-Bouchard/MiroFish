class FormHandler(BaseHTTPRequestHandler):
    def _redirect(self, message: str, tab: str = "infra") -> None:
        location = "/?tab=" + urllib.parse.quote(tab) + "&msg=" + urllib.parse.quote(message)
        self.send_response(303)
        self.send_header("Location", location)
        self.end_headers()

    def _read_form_urlencoded(self) -> dict[str, str]:
        length = int(self.headers.get("Content-Length", "0"))
        raw = self.rfile.read(length).decode("utf-8", errors="ignore")
        parsed = urllib.parse.parse_qs(raw)
        return {k: (v[-1] if v else "") for k, v in parsed.items()}

    def do_GET(self) -> None:
        parsed = urllib.parse.urlparse(self.path)
        query = urllib.parse.parse_qs(parsed.query)
        tab = (query.get("tab", ["infra"])[0] or "infra").strip()
        msg = (query.get("msg", [""])[0] or "").strip()
        html_doc = render_page(message=msg, active_tab=tab)

        self.send_response(200)
        self.send_header("Content-Type", "text/html; charset=utf-8")
        self.send_header("Content-Length", str(len(html_doc.encode("utf-8"))))
        self.end_headers()
        self.wfile.write(html_doc.encode("utf-8"))

    def do_POST(self) -> None:
        if self.path == "/api/setup":
            try:
                data = self._read_form_urlencoded()
                fp = int(data.get("frontend_port", "0"))
                bp = int(data.get("backend_port", "0"))

                if fp == bp:
                    raise ValueError("Frontend and backend ports must be different")
                validate_port(fp, "Frontend host port")
                validate_port(bp, "Backend host port")

                values = {
                    "LLM_API_KEY": data.get("LLM_API_KEY", "").strip(),
                    "ZEP_API_KEY": data.get("ZEP_API_KEY", "").strip(),
                    "LLM_BASE_URL": data.get("LLM_BASE_URL", "").strip(),
                    "LLM_MODEL_NAME": data.get("LLM_MODEL_NAME", "").strip(),
                    "LLM_BOOST_ENABLED": "false",
                }
                if not values["LLM_API_KEY"] or not values["ZEP_API_KEY"]:
                    raise ValueError("LLM_API_KEY and ZEP_API_KEY are required")

                write_env_file(values)
                generate_compose(fp, bp)
                self._redirect("Saved infrastructure config", tab="infra")
            except Exception as exc:
                self._redirect(f"Error: {exc}", tab="infra")
            return

        if self.path in {"/api/whitelist/add", "/api/whitelist/remove"}:
            try:
                data = self._read_form_urlencoded()
                asset_rel = data.get("asset_rel", "").strip()
                if not asset_rel:
                    raise ValueError("asset_rel required")

                assets = discover_image_assets()
                known = {str(a["rel_path"]) for a in assets}
                if asset_rel not in known:
                    raise ValueError("unknown asset")

                wl = load_whitelist(assets)
                if self.path.endswith("/add"):
                    wl.add(asset_rel)
                else:
                    wl.discard(asset_rel)
                save_whitelist(wl)
                self._redirect("Whitelist updated", tab="branding")
            except Exception as exc:
                self._redirect(f"Error: {exc}", tab="branding")
            return

        if self.path == "/api/replace":
            try:
                ctype = self.headers.get("Content-Type", "")
                if "multipart/form-data" not in ctype:
                    raise ValueError("multipart/form-data required")

                boundary_match = re.search(r"boundary=(.+)", ctype)
                if not boundary_match:
                    raise ValueError("multipart boundary missing")

                length = int(self.headers.get("Content-Length", "0"))
                body = self.rfile.read(length)
                boundary = boundary_match.group(1).encode()
                parts = body.split(b"--" + boundary)

                asset_rel = ""
                upload_name = ""
                upload_bytes = b""

                for part in parts:
                    if b"Content-Disposition" not in part:
                        continue
                    header, _, content = part.partition(b"\r\n\r\n")
                    header_text = header.decode("utf-8", errors="ignore")
                    content = content.rstrip(b"\r\n")
                    if 'name="asset_rel"' in header_text:
                        asset_rel = content.decode("utf-8", errors="ignore").strip()
                    elif 'name="replacement"' in header_text:
                        m = re.search(r'filename="([^"]*)"', header_text)
                        upload_name = m.group(1) if m else "upload.bin"
                        upload_bytes = content

                if not asset_rel:
                    raise ValueError("asset_rel missing")

                assets = discover_image_assets()
                wl = load_whitelist(assets)
                message = replace_asset(asset_rel, upload_name, upload_bytes, wl)
                self._redirect(message, tab="branding")
            except Exception as exc:
                self._redirect(f"Error: {exc}", tab="branding")
            return

        self._redirect("Unknown endpoint", tab="infra")


def run_form_server(port: int) -> None:
    validate_port(port, "Form server port")
    server = HTTPServer(("0.0.0.0", port), FormHandler)
    print(f"[ok] Form UI: http://localhost:{port}")
    print("[info] Tabs: Infrastructure + Branding (whitelabeling)")
    print("[info] Press Ctrl+C to stop")
    try:
        server.serve_forever()
    except KeyboardInterrupt:
        pass
    finally:
        server.server_close()


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="MiroFish tabbed infrastructure + whitelabeling form server"
    )
    parser.add_argument(
        "--port",
        type=int,
        default=8777,
        help="Port for local form server (default: 8777)",
    )
    return parser


def main() -> int:
    args = build_parser().parse_args()
    try:
        run_form_server(args.port)
        return 0
    except KeyboardInterrupt:
        return 130
    except Exception as exc:
        print(f"[fatal] {exc}")
        return 1


if __name__ == "__main__":
    raise SystemExit(main())
