def is_port_in_use(port: int) -> bool:
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as s:
        s.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)
        return s.connect_ex(("127.0.0.1", port)) == 0


def validate_port(port: int, label: str) -> None:
    if port <= 0 or port > 65535:
        raise ValueError(f"{label} must be between 1 and 65535")
    if port in DISALLOWED_PORTS:
        raise ValueError(f"{label}={port} is disallowed")
    if is_port_in_use(port):
        raise ValueError(f"{label}={port} is already in use")


def ratio_text(width: int | None, height: int | None) -> str:
    if not width or not height:
        return "unknown"
    g = math.gcd(width, height)
    return f"{width // g}:{height // g}"


def _parse_svg_size(path: Path) -> tuple[int | None, int | None]:
    try:
        root = ET.fromstring(path.read_text(encoding="utf-8", errors="ignore"))
    except Exception:
        return None, None

    def n(v: str | None) -> int | None:
        if not v:
            return None
        cleaned = re.sub(r"[^0-9.]+", "", v)
        if not cleaned:
            return None
        try:
            return int(float(cleaned))
        except ValueError:
            return None

    width = n(root.attrib.get("width"))
    height = n(root.attrib.get("height"))
    if width and height:
        return width, height

    view_box = root.attrib.get("viewBox")
    if view_box:
        parts = view_box.replace(",", " ").split()
        if len(parts) == 4:
            try:
                return int(float(parts[2])), int(float(parts[3]))
            except ValueError:
                return None, None
    return None, None


def parse_image_size(path: Path) -> tuple[int | None, int | None]:
    suffix = path.suffix.lower()
    data = path.read_bytes()

    if suffix == ".png" and len(data) >= 24 and data[:8] == b"\x89PNG\r\n\x1a\n":
        return int.from_bytes(data[16:20], "big"), int.from_bytes(data[20:24], "big")

    if suffix in {".jpg", ".jpeg"} and data[:2] == b"\xff\xd8":
        i = 2
        while i + 9 < len(data):
            if data[i] != 0xFF:
                i += 1
                continue
            marker = data[i + 1]
            i += 2
            if marker in {0xD8, 0xD9}:
                continue
            if i + 2 > len(data):
                break
            seg_len = int.from_bytes(data[i:i + 2], "big")
            if seg_len < 2 or i + seg_len > len(data):
                break
            if marker in {0xC0, 0xC1, 0xC2, 0xC3, 0xC5, 0xC6, 0xC7, 0xC9, 0xCA, 0xCB, 0xCD, 0xCE, 0xCF}:
                h = int.from_bytes(data[i + 3:i + 5], "big")
                w = int.from_bytes(data[i + 5:i + 7], "big")
                return w, h
            i += seg_len

    if suffix == ".gif" and len(data) >= 10 and data[:3] == b"GIF":
        return int.from_bytes(data[6:8], "little"), int.from_bytes(data[8:10], "little")

    if suffix == ".bmp" and len(data) >= 26 and data[:2] == b"BM":
        return int.from_bytes(data[18:22], "little"), abs(int.from_bytes(data[22:26], "little", signed=True))

    if suffix == ".webp" and len(data) >= 30 and data[:4] == b"RIFF" and data[8:12] == b"WEBP":
        chunk = data[12:16]
        if chunk == b"VP8X" and len(data) >= 30:
            w = 1 + int.from_bytes(data[24:27], "little")
            h = 1 + int.from_bytes(data[27:30], "little")
            return w, h

    if suffix == ".ico" and len(data) >= 8:
        w = data[6] or 256
        h = data[7] or 256
        return w, h

    if suffix == ".svg":
        return _parse_svg_size(path)

    return None, None


def find_text_references(asset_rel: str) -> list[str]:
    refs: list[str] = []
    needle = asset_rel.replace("\\", "/")
    basename = Path(asset_rel).name

    for path in ROOT.rglob("*"):
        if not path.is_file() or path.suffix.lower() not in TEXT_SEARCH_EXTENSIONS:
            continue
        try:
            content = path.read_text(encoding="utf-8", errors="ignore")
        except Exception:
            continue
        if needle in content or basename in content:
            refs.append(str(path.relative_to(ROOT)))
        if len(refs) >= 8:
            break
    return refs


def discover_image_assets() -> list[dict[str, object]]:
    assets: list[dict[str, object]] = []
    for path in ROOT.rglob("*"):
        if not path.is_file() or path.suffix.lower() not in IMAGE_EXTENSIONS:
            continue
        rel = str(path.relative_to(ROOT)).replace("\\", "/")
        lower = rel.lower()
        width, height = parse_image_size(path)
        refs = find_text_references(rel)
        is_branding = any(h in lower for h in BRANDING_HINTS) or bool(refs)

        assets.append(
            {
                "rel_path": rel,
                "size_bytes": path.stat().st_size,
                "width": width,
                "height": height,
                "ratio": ratio_text(width, height),
                "is_branding": is_branding,
                "references": refs,
            }
        )

    assets.sort(key=lambda x: str(x["rel_path"]))
    return assets


def default_whitelist(assets: list[dict[str, object]]) -> set[str]:
    items = set()
    for asset in assets:
        rel = str(asset["rel_path"])
        if asset.get("is_branding"):
            items.add(rel)
    return items


def load_whitelist(assets: list[dict[str, object]]) -> set[str]:
    if WHITELIST_PATH.exists():
        try:
            data = json.loads(WHITELIST_PATH.read_text(encoding="utf-8"))
            listed = data.get("assets", [])
            return {str(x) for x in listed}
        except Exception:
            pass
    wl = default_whitelist(assets)
    save_whitelist(wl)
    return wl


def save_whitelist(items: set[str]) -> None:
    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    payload = {"assets": sorted(items), "max_upload_mb": MAX_UPLOAD_MB}
    WHITELIST_PATH.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def safe_asset_path(asset_rel: str) -> Path:
    target = (ROOT / asset_rel).resolve()
    root_resolved = ROOT.resolve()
    if root_resolved not in [target] and root_resolved not in target.parents:
        raise ValueError("Asset path is outside project root")
    if not target.exists() or not target.is_file():
        raise ValueError("Target asset does not exist")
    if target.suffix.lower() not in IMAGE_EXTENSIONS:
        raise ValueError("Target must be an image asset")
    return target


def replace_asset(asset_rel: str, upload_name: str, upload_bytes: bytes, whitelist: set[str]) -> str:
    if asset_rel not in whitelist:
        raise ValueError("Asset is not whitelisted for replacement")

    target = safe_asset_path(asset_rel)
    if not upload_bytes:
        raise ValueError("No file uploaded")

    if len(upload_bytes) > MAX_UPLOAD_MB * 1024 * 1024:
        raise ValueError(f"Upload too large (> {MAX_UPLOAD_MB}MB)")

    upload_suffix = Path(upload_name).suffix.lower()
    if upload_suffix not in IMAGE_EXTENSIONS:
        raise ValueError("Uploaded file type is not allowed")

    temp_path = GENERATED_DIR / f"upload-check-{datetime.datetime.now().strftime('%Y%m%d%H%M%S%f')}{upload_suffix}"
    GENERATED_DIR.mkdir(parents=True, exist_ok=True)
    temp_path.write_bytes(upload_bytes)

    try:
        old_w, old_h = parse_image_size(target)
        new_w, new_h = parse_image_size(temp_path)

        if old_w and old_h and new_w and new_h:
            old_ratio = old_w / old_h
            new_ratio = new_w / new_h
            if abs(old_ratio - new_ratio) > RATIO_TOLERANCE:
                raise ValueError(
                    f"Aspect ratio mismatch: old={ratio_text(old_w, old_h)} new={ratio_text(new_w, new_h)}"
                )

        backup_stamp = datetime.datetime.now().strftime("%Y%m%d%H%M%S")
        backup_target = ASSET_BACKUP_DIR / backup_stamp / asset_rel
        backup_target.parent.mkdir(parents=True, exist_ok=True)
        backup_target.write_bytes(target.read_bytes())

        target.write_bytes(upload_bytes)
    finally:
        if temp_path.exists():
            temp_path.unlink()

    return f"Replaced {asset_rel} (backup saved)"


def read_urlencoded(handler: BaseHTTPRequestHandler) -> dict[str, str]:
    length = int(handler.headers.get("Content-Length", "0"))
    payload = handler.rfile.read(length).decode("utf-8", errors="ignore")
    parsed = urllib.parse.parse_qs(payload)
    return {k: (v[-1] if v else "") for k, v in parsed.items()}


